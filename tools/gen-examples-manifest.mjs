import fs from "node:fs";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const examplesDir = path.join(root, "examples");
const outputs = [
  path.join(root, "libs/frothy-editor/src/examples.generated.ts"),
  path.join(root, "editors/vscode/src/examples.generated.ts"),
];

export function parseExample(name, text) {
  let title;
  const blurb = [];
  let tags;

  for (const line of text.split(/\r?\n/)) {
    const comment = line.match(/^\s*--\s?(.*)$/);
    if (!comment) {
      if (line.trim() === "" && title !== undefined) break;
      if (line.trim() !== "") break;
      continue;
    }

    const body = comment[1].trim();
    const tag = body.match(/^@tag(?:\s+(.*))?$/);
    if (tag) {
      tags = tag[1]?.trim().split(/\s+/).filter(Boolean) ?? [];
      break;
    }

    if (title === undefined) {
      title = body;
    } else {
      blurb.push(body);
    }
  }

  if (!title) throw new Error(`${name}: missing title line`);
  if (!tags) throw new Error(`${name}: missing -- @tag line`);
  if (tags.length === 0) throw new Error(`${name}: missing @tag tokens`);

  return {
    name,
    title,
    blurb: blurb.join(" ").trim(),
    tags,
    source: text,
  };
}

function readExamples() {
  return fs.readdirSync(examplesDir)
    .filter((file) => file.endsWith(".fr"))
    .sort()
    .map((file) => {
      const name = path.basename(file, ".fr");
      const text = fs.readFileSync(path.join(examplesDir, file), "utf8");
      return parseExample(name, text);
    });
}

function moduleText(examples) {
  return `// GENERATED by tools/gen-examples-manifest.mjs from examples/*.fr.
// Do not edit by hand; run \`make examples-manifest\`.
export interface FrothyExample {
  name: string;
  title: string;
  blurb: string;
  tags: string[];
  source: string;
}
export const FROTHY_EXAMPLES: readonly FrothyExample[] = ${JSON.stringify(examples, null, 2)};
`;
}

function main() {
  const text = moduleText(readExamples());
  for (const out of outputs) {
    fs.mkdirSync(path.dirname(out), { recursive: true });
    fs.writeFileSync(out, text);
  }
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  try {
    main();
  } catch (err) {
    console.error(err instanceof Error ? err.message : String(err));
    process.exit(1);
  }
}
